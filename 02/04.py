print("This program will give you the sum, product and average of three integer numbers.")

n1 = int(input("Enter the first number: "))
n2 = int(input("Enter the second number: "))
n3 = int(input("Enter the third number: "))

print(f"The sum of the numbers is {n1 + n2 + n3}")
print(f"The product of the numbers is {n1 * n2 * n3}")
print(f"The average of the numbers is {(n1 + n2 + n3) / 3}")
